import sys
from math import gcd
from collections import Counter


def analyze_crypto_sequence(keys_hex):
    keys = [int(k, 16) for k in keys_hex]
    n = len(keys)
    print(f"📊 Анализ последовательности ({n} значений). Диапазон: {keys[0]} ... {keys[-1]}")
    print("=" * 60)

    # 1. Проверка аффинного роста (без модуля): a[n] = A*a[n-1] + B
    print("\n🔍 1. Проверка аффинного роста a[n] = A·a[n-1] + B")
    if keys[1] != keys[0]:
        A_est = (keys[2] - keys[1]) / (keys[1] - keys[0])
        B_est = keys[1] - A_est * keys[0]
        print(f"   Оценочные параметры: A ≈ {A_est:.4f}, B ≈ {B_est:.2f}")

        # Проверка стабильности A и B
        A_vals = [(keys[i] - keys[i - 1]) / (keys[i - 1] - keys[i - 2]) for i in range(2, min(10, n))]
        B_vals = [keys[i] - A_est * keys[i - 1] for i in range(1, min(10, n))]
        print(f"   Разброс A: {min(A_vals):.4f} ... {max(A_vals):.4f}")
        print(f"   Разброс B: {min(B_vals):.1f} ... {max(B_vals):.1f}")
        if max(A_vals) - min(A_vals) < 0.01 and max(B_vals) - min(B_vals) < 1000:
            print("   ✅ Стабильный аффинный рост!")
        else:
            print("   ❌ Рост нестабилен. Вероятно, нелинейность или модуль.")

    # 2. Взлом LCG (с модулем или без)
    print("\n🔍 2. Поиск параметров LCG: a[n] = (A·a[n-1] + B) % M")
    diffs = [keys[i] - keys[i - 1] for i in range(1, n)]
    if all(d != 0 for d in diffs):
        # Метод GCD для восстановления модуля и множителя
        g = 0
        for i in range(len(diffs) - 1):
            val = diffs[i + 1] * diffs[i - 1] - diffs[i] ** 2
            g = gcd(g, abs(val))

        if g > 1 and g < 2 ** 260:
            MOD = g
            # Находим A по модулю
            inv = pow(diffs[0], -1, MOD)
            A_lcg = (diffs[1] * inv) % MOD
            B_lcg = (keys[1] - A_lcg * keys[0]) % MOD
            print(f"   🎯 Найдено! M = {MOD}")
            print(f"      A = {A_lcg}\n      B = {B_lcg}")

            # Верификация
            errors = sum(1 for i in range(2, n) if keys[i] != (A_lcg * keys[i - 1] + B_lcg) % MOD)
            if errors == 0:
                print("   ✅ LCG полностью совпадает!")
            else:
                print(f"   ⚠️ {errors} расхождений. Возможно, M составной или есть доп. шаг.")
        else:
            print("   ❌ Модуль LCG не найден (последовательность не LCG или M слишком большой).")

    # 3. Анализ битовой структуры (LFSR / XorShift)
    print("\n🔍 3. Битовый анализ (LFSR/XorShift)")
    bit_diffs = [keys[i] ^ keys[i - 1] for i in range(1, min(15, n))]
    shifts = [bit_diffs[i] >> bit_diffs[i - 1].bit_length() if bit_diffs[i - 1] > 0 else 0 for i in
              range(1, len(bit_diffs))]
    print(f"   XOR-разности первых шагов (hex): {[hex(d) for d in bit_diffs[:5]]}")
    print(f"   Сдвиги/Маски: {shifts[:5]}")

    if any(d.bit_count() < 20 for d in bit_diffs):
        print("   ✅ Низкая энтропия в разностях → возможен XorShift или табличный генератор.")
    else:
        print("   ❌ Высокая энтропия → вероятен хеш, шифр или сложная нелинейная функция.")

    # 4. Итог
    print("\n📌 ИТОГ:")
    print("   Ваши float-коэффициенты — это аппроксимация тренда роста.")
    print("   Для точной генерации/предсказания ключей используйте:")
    print("   • Целочисленную арифметику (int)")
    print("   • Модуль M (если есть обёртка)")
    print("   • LCG/XorShift/LFSR вместо вещественных рекуррент")
    print("   • Если это Bitcoin Puzzle → проверьте документацию сканера (stride, offset pattern)")


