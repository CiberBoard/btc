def hex_to_dec(h):
    return int(h, 16)

def dec_to_hex(d):
    return f"{d:064x}"

def get_range(n):
    return 2**(n-1), 2**n - 1

def kde_estimate(data, bandwidth=0.05, points=1000):
    """Простая KDE без scipy"""
    xs = np.linspace(0, 1, points)
    density = np.zeros_like(xs)

    for d in data:
        density += np.exp(-0.5 * ((xs - d) / bandwidth) ** 2)

    density /= (len(data) * bandwidth * np.sqrt(2 * np.pi))
    return xs, density

