# ═══════════════════════════════════════════════
# 5. ПРОГНОЗ ДИАПАЗОНА
# ═══════════════════════════════════════════════

next_puzzle = len(keys) + 1
pmin, pmax = get_range(next_puzzle)

# квантильный диапазон (20% - 80%)
low_q = np.quantile(positions, 0.2)
high_q = np.quantile(positions, 0.8)

pred_min = int(pmin + low_q * (pmax - pmin))
pred_max = int(pmin + high_q * (pmax - pmin))

