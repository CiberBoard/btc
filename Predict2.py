# 2. ФУНКЦИИ
# ═══════════════════════════════════════════════

def hex_to_dec(h):
    return int(h, 16)

def dec_to_hex(d):
    return f"{d:064x}"

def get_range(n):
    return 2**(n-1), 2**n - 1

# ═══════════════════════════════════════════════
# 3. ПОДГОТОВКА
# ═══════════════════════════════════════════════

keys = [hex_to_dec(k) for k in KNOWN_KEYS_HEX]

positions = []

# пропускаем первый пазл (деление на 0)
for i, key in enumerate(keys[1:], start=2):
    pmin, pmax = get_range(i)

    range_size = pmax - pmin
    pos = (key - pmin) / range_size if range_size != 0 else 0.0

    positions.append(pos)

positions = np.array(positions)

# ═══════════════════════════════════════════════
